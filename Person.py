class Person:
    def __init__(self, username, email, fname, lname, password, phonenumber, city, birthday, securitytext, id) -> None:
        self.username = username
        self.email = email
        self.fname = fname
        self.lname = lname
        self.password = password
        self.phonenumber = phonenumber
        self.city = city
        self.birthday = birthday
        self.securitytext = securitytext
        self.id = id